# permutations 函数生成所有数字的排列，然后遍历每个排列，将数字赋值给汉字，构造算式，检查是否满足等式，将满足等式的结果添加到结果列表中
from itertools import permutations

# 可选数字
# 注意：这里的数字不能重复
digits = [1, 3, 4, 5, 6, 8, 9]
# 存储结果
# 每个元素是一个元组，元组中每个元素是一个汉字的具体数字
results = []

# 遍历所有数字的排列
for perm in permutations(digits):
    # 将数字赋值给汉字
    学, 语, 文, 大, 家, 都, 爱 = perm

    # 构造算式
    # 注意：这里的变量名必须与汉字相同
    学语文 = 学 * 100 + 语 * 10 + 文
    文数 = 文
    大家都爱 = 大 * 1000 + 家 * 100 + 都 * 10 + 爱

    # 检查是否满足等式，如果满足则将结果添加到结果列表中
    if 学语文 * 文数 == 大家都爱:
        results.append((学, 语, 文, 大, 家, 都, 爱))

print(results)